#include "image.h"

using namespace so_gpu ;

//*****************************************************************************************************
image::image( void_t ) 
{}

//*****************************************************************************************************
image::image( this_rref_t rhv ) : object( std::move(rhv) )
{
    _image_format = rhv._image_format ;
    _image_type = rhv._image_type ;
    _pixel_type = rhv._pixel_type ;
}

//*****************************************************************************************************
image::~image( void_t ) 
{

}

//*****************************************************************************************************
void_t image::set_type( image_type it ) 
{
    _image_type = it ;
}

//*****************************************************************************************************
image_type image::get_type( void_t ) const 
{
    return _image_type ;
}

//*****************************************************************************************************
void_t image::set_format( image_format imgf ) 
{
    _image_format = imgf ;
}

//*****************************************************************************************************
image_format image::get_format( void_t ) const 
{
    return _image_format ;
}

//*****************************************************************************************************
pixel_type image::get_pixel_type( void_t ) const 
{
    return _pixel_type ;
}

//*****************************************************************************************************
void_t image::set_pixel_type( pixel_type t ) 
{
    _pixel_type = t ;
}

