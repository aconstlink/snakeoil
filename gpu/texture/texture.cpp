#include "texture.h"

using namespace so_gpu ;

//*****************************************************************************************************
texture::texture( void_t ) 
{

}

//*****************************************************************************************************
texture::texture( texture && rhv ) : base_t( std::move( rhv ) )
{
    _has_changed = rhv._has_changed ;
}

//*****************************************************************************************************
texture::~texture( void_t ) 
{
}

//*****************************************************************************************************
bool_t texture::has_state_changed( void_t ) const 
{
    return _has_changed ;
}

//*****************************************************************************************************
void_t texture::set_state_changed( bool_t b ) 
{
    _has_changed = b ;
}

